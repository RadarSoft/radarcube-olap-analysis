namespace RadarSoft.RadarCube.Enums
{
    /// <summary>Evaluates the display mode for subtotal values.</summary>
    public enum TotalAppearance
    {
        /// <summary>
        ///     Total line is the first line in the area of adequate comparisons.
        /// </summary>
        taFirst,

        /// <summary>
        ///     Total line is the last line in the area of adequate comparisons.
        /// </summary>
        taLast,

        /// <summary>
        ///     Total line is not displayed in the grid.
        /// </summary>
        taInvisible
    }
}